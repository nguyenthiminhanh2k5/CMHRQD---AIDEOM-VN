def unpack(z):
    return z[0*T : 1*T], z[1*T : 2*T], z[2*T : 3*T], z[3*T : 4*T], z[4*T : 5*T]

def simulate(z):
    I_K, I_D, I_AI, I_H, C = unpack(z)
    K, D, AI = np.zeros(T+1), np.zeros(T+1), np.zeros(T+1)
    H, A, Y = np.zeros(T+1), np.zeros(T+1), np.zeros(T)
    K[0], D[0], AI[0] = K0, D0, AI0
    H[0], A[0] = H0, A0

    for t in range(T):
        Y[t] = (A[t] * K[t]**alpha * L0**beta_L * D[t]**gamma_D * AI[t]**delta_AI_prod * H[t]**theta_H_prod)
        K[t+1] = (1-dK)*K[t] + I_K[t]
        D[t+1] = (1-dD)*D[t] + I_D[t]
        AI[t+1] = (1-dAI)*AI[t] + I_AI[t]
        H[t+1] = H[t] + theta_H*I_H[t] - mu*H[t]
        A[t+1] = A[t] * (1 + phi1*D[t] + phi2*AI[t] + phi3*H[t])
    return K, D, AI, H, A, Y

def objective(z):
    _, _, _, _, C = unpack(z)
    return -np.sum([rho**t * np.log(np.maximum(C[t], 1e-6)) for t in range(T)])

