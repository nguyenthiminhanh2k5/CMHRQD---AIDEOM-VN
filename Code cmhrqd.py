def m1_economic_forecast(ratios, n_years=N_YEARS_B12, budget=BUDGET_ANNUAL_B12):
    """
    Module M1 – Dự báo kinh tế Việt Nam 2026–2030
    ratios : dict {'K','D','AI','H'} với tổng bằng 1
    """
    required_keys = {'K', 'D', 'AI', 'H'}
    assert set(ratios.keys()) == required_keys, (
        "ratios phải gồm đúng các khóa: {'K','D','AI','H'}"
    )
    assert abs(sum(ratios.values()) - 1.0) < 1e-9, (
        "Tổng tỷ trọng ngân sách phải bằng 1"
    )

    K, L, D, AI, H, A = K0_B12, L0_B12, D0_B12, AI0_B12, H0_B12, A0_B12
    years = list(range(2026, 2026 + n_years))

    GDP_path, K_path, D_path = [], [], []
    AI_path, H_path, A_path = [], [], []

    for year in years:
        Y = (
            A * (K**ALPHA_B12) * (L**BETA_L_B12) * (D**GAMMA_D_B12)
            * (AI**DELTA_AI_B12) * (H**THETA_H_B12)
        )

        GDP_path.append(Y)
        K_path.append(K)
        D_path.append(D)
        AI_path.append(AI)
        H_path.append(H)
        A_path.append(A)

        if year == years[-1]:
            break

        invest_K  = ratios['K']  * budget
        invest_D  = ratios['D']  * budget
        invest_AI = ratios['AI'] * budget
        invest_H  = ratios['H']  * budget

        K = (1 - DK_B12) * K + invest_K
        D = min((1 - DD_B12) * D + invest_D / 500, 50.0)
        AI = (1 - DAI_B12) * AI + invest_AI / 15
        H = H + THETA_H_ACC_B12 * invest_H / 300 - MU_B12 * H
        H = float(np.clip(H, 5.0, 65.0))

        A = A * (1 + PHI1_B12 * D + PHI2_B12 * AI + PHI3_B12 * H)

    GDP_start, GDP_2030 = GDP_path[0], GDP_path[-1]
    CAGR = (GDP_2030 / GDP_start) ** (1 / (n_years - 1)) - 1

    return {
        'years': years,
        'GDP_path': GDP_path, 'K_path': K_path, 'D_path': D_path,
        'AI_path': AI_path, 'H_path': H_path, 'A_path': A_path,
        'GDP_start': round(GDP_start, 2),
        'GDP_2030': round(GDP_2030, 2),
        'CAGR_pct': round(CAGR * 100, 3),
        'D_2030': round(D_path[-1], 2),
        'AI_2030': round(AI_path[-1], 2),
        'H_2030': round(H_path[-1], 2),
        'A_2030': round(A_path[-1], 4),
    }

