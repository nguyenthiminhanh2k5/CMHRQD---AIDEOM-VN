def m3_budget_optimization(total_budget=50000, gamma_eq=0.007, lam_eq=0.7):
    """
    Module M3 – LP phân bổ ngân sách số: 6 vùng × 4 hạng mục
    Tái sử dụng mô hình Bài 4 (PuLP + CBC), có ràng buộc công bằng C5.

    Parameters
    ----------
    total_budget : float  tổng ngân sách (tỷ VND), mặc định 50,000
    gamma_eq     : float  hệ số chỉ số số hóa trong C5
    lam_eq       : float  ngưỡng công bằng (0.7 = vùng yếu ≥ 70% vùng mạnh)

    Returns
    -------
    alloc_df : DataFrame  ma trận 6×4 (tỷ VND)
    z_star   : float      Z* (GDP gain tối ưu, tỷ VND)
    ratios   : dict       tỷ lệ đầu tư tổng hợp {'K','D','AI','H'}
    """
    regions = ['NMM', 'RRD', 'NCC', 'CH', 'SE', 'MD']
    items   = ['I', 'D', 'AI', 'H']
    region_full = {
        'NMM': 'Northern Midlands & Mtn',
        'RRD': 'Red River Delta',
        'NCC': 'N/S Central Coast',
        'CH' : 'Central Highlands',
        'SE' : 'Southeast',
        'MD' : 'Mekong Delta'
    }
    beta_dict = {
        ('NMM','I'):1.15, ('NMM','D'):0.85, ('NMM','AI'):0.55, ('NMM','H'):1.30,
        ('RRD','I'):0.95, ('RRD','D'):1.25, ('RRD','AI'):1.40, ('RRD','H'):1.05,
        ('NCC','I'):1.05, ('NCC','D'):0.95, ('NCC','AI'):0.85, ('NCC','H'):1.15,
        ('CH' ,'I'):1.20, ('CH' ,'D'):0.75, ('CH' ,'AI'):0.45, ('CH' ,'H'):1.35,
        ('SE' ,'I'):0.90, ('SE' ,'D'):1.30, ('SE' ,'AI'):1.55, ('SE' ,'H'):1.00,
        ('MD' ,'I'):1.10, ('MD' ,'D'):0.85, ('MD' ,'AI'):0.65, ('MD' ,'H'):1.25,
    }
    D0_map = {'NMM':38, 'RRD':78, 'NCC':55, 'CH':32, 'SE':82, 'MD':48}

    m = pulp.LpProblem('M3_LP_B12', pulp.LpMaximize)
    x = pulp.LpVariable.dicts('x', (regions, items), lowBound=0)
    M_dmax = pulp.LpVariable('D_max', lowBound=0)

    m += pulp.lpSum(beta_dict[(r,j)] * x[r][j]
                    for r in regions for j in items)
    m += pulp.lpSum(x[r][j] for r in regions for j in items) == total_budget
    for r in regions:
        m += pulp.lpSum(x[r][j] for j in items) >= 5000
        m += pulp.lpSum(x[r][j] for j in items) <= 12000
    m += pulp.lpSum(x[r]['H'] for r in regions) >= 12000
    for r in regions:
        m += D0_map[r] + gamma_eq * x[r]['D'] <= M_dmax
        m += D0_map[r] + gamma_eq * x[r]['D'] >= lam_eq * M_dmax

    m.solve(pulp.PULP_CBC_CMD(msg=False))

    status = pulp.LpStatus[m.status]
    if status != 'Optimal':
        raise RuntimeError(f"M3 LP không tối ưu: {status}")

    alloc_data = {
        r: {j: round(float(x[r][j].value()), 2) for j in items}
        for r in regions
    }
    alloc_df = pd.DataFrame(alloc_data).T
    alloc_df.index   = [region_full[r] for r in regions]
    alloc_df.columns = items

    tot = {j: sum(float(x[r][j].value()) for r in regions) for j in items}
    grand = sum(tot.values())
    if grand <= 1e-9:
      raise ValueError(
        "M3: tổng phân bổ bằng 0"
    )
    ratios_out = {
        'K' : tot['I']  / grand,
        'D' : tot['D']  / grand,
        'AI': tot['AI'] / grand,
        'H' : tot['H']  / grand,
    }

    return alloc_df, float(pulp.value(m.objective)), ratios_out

