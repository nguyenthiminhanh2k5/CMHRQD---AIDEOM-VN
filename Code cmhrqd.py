def m2_digital_readiness(df_regions):
    """
    Module M2 – Xếp hạng Sẵn sàng số 6 vùng kinh tế-xã hội
    """
    criteria = [
        'grdp_per_capita_million_VND', 'fdi_registered_billion_USD',
        'digital_index_0_100', 'ai_readiness_0_100', 'trained_labor_pct',
        'rd_intensity_pct', 'internet_penetration_pct', 'gini_coef'
    ]
    is_benefit = [True, True, True, True, True, True, True, False]
    expert_weights = np.array([0.10, 0.10, 0.15, 0.20, 0.15, 0.15, 0.05, 0.10])

    missing_cols = [c for c in criteria if c not in df_regions.columns]
    if missing_cols:
        raise ValueError(f"M2: thiếu cột dữ liệu {missing_cols}")

    X = df_regions[criteria].astype(float).values
    if np.isnan(X).any() or (X < 0).any():
        raise ValueError("M2: dữ liệu chứa giá trị NaN hoặc giá trị âm")

    X_entropy = X.copy()
    for j, benefit in enumerate(is_benefit):
        if not benefit:
            X_entropy[:, j] = X_entropy[:, j].max() - X_entropy[:, j]

    P = X_entropy / (X_entropy.sum(axis=0) + 1e-12)
    k_entropy = 1.0 / np.log(len(X_entropy))
    entropy = -k_entropy * np.nansum(P * np.log(P + 1e-12), axis=0)

    diversification = 1 - entropy
    entropy_weights = diversification / diversification.sum()

    def topsis_score(X_input, weights, benefit_flags):
        R = X_input / np.sqrt((X_input**2).sum(axis=0) + 1e-12)
        V = R * weights

        A_plus = np.where(benefit_flags, V.max(axis=0), V.min(axis=0))
        A_minus = np.where(benefit_flags, V.min(axis=0), V.max(axis=0))

        S_plus = np.sqrt(((V - A_plus)**2).sum(axis=1))
        S_minus = np.sqrt(((V - A_minus)**2).sum(axis=1))
        return S_minus / (S_plus + S_minus + 1e-12)

    score_expert = topsis_score(X, expert_weights, is_benefit)
    score_entropy = topsis_score(X, entropy_weights, is_benefit)

    ranking_df = df_regions[['region_name_en']].copy().reset_index(drop=True)
    ranking_df['Score_Expert'] = score_expert.round(4)
    ranking_df['Score_Entropy'] = score_entropy.round(4)

    ranking_df['Rank_Expert'] = (
        pd.Series(score_expert).rank(ascending=False, method='first')
        .astype(int).values
    )
    ranking_df['Rank_Entropy'] = (
        pd.Series(score_entropy).rank(ascending=False, method='first')
        .astype(int).values
    )
    ranking_df['Rank_Change'] = (
        ranking_df['Rank_Expert'] - ranking_df['Rank_Entropy']
    )
    ranking_df = ranking_df.sort_values('Rank_Expert').reset_index(drop=True)

    weights_df = pd.DataFrame({
        'Criterion': criteria,
        'Expert_Weight': np.round(expert_weights, 4),
        'Entropy_Weight': np.round(entropy_weights, 4)
    })

    return ranking_df, weights_df

