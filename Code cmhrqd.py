import gymnasium as gym
from gymnasium import spaces
import numpy as np

class VietnamEconomyEnv(gym.Env):
    metadata = {"render_modes": []}

    def __init__(self):
        super().__init__()
        self.action_space = spaces.Discrete(5)
        self.observation_space = spaces.MultiDiscrete([3, 3, 3, 3])
        self.T = 10

        self.allocation = {
            0: np.array([0.70, 0.10, 0.10, 0.10]),
            1: np.array([0.40, 0.25, 0.15, 0.20]),
            2: np.array([0.25, 0.45, 0.15, 0.15]),
            3: np.array([0.20, 0.20, 0.45, 0.15]),
            4: np.array([0.30, 0.20, 0.10, 0.40])
        }

        self.w_gdp, self.w_unemp, self.w_cyber, self.w_emiss = 0.40, 0.25, 0.20, 0.15
        self.alpha, self.beta_L, self.gamma_D, self.delta_AI, self.theta_H = 0.33, 0.42, 0.10, 0.08, 0.07
        self.dK, self.dD, self.dAI = 0.05, 0.08, 0.10
        self.mu, self.theta_H_acc = 0.02, 0.80
        self.phi1, self.phi2, self.phi3 = 0.0008, 0.0006, 0.0010

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)
        self.K, self.L, self.D, self.AI, self.H, self.A = 27500.0, 53.9, 20.3, 86.0, 30.0, 1.50
        self.t = 0
        self.growth_rate = 6.5
        self.Y_prev = self._compute_Y()
        self.state = self._get_state()
        return self.state.copy(), {}

    def _compute_Y(self):
        return self.A * self.K**self.alpha * self.L**self.beta_L * self.D**self.gamma_D * self.AI**self.delta_AI * self.H**self.theta_H

    def _get_state(self):
        g_disc = 0 if self.growth_rate < 5 else (1 if self.growth_rate < 8 else 2)
        d_disc = 0 if self.D < 18 else (1 if self.D < 25 else 2)
        ai_disc = 0 if self.AI < 80 else (1 if self.AI < 120 else 2)
        u_disc = 2 if self.growth_rate < 4 else (1 if self.growth_rate < 7 else 0)
        return np.array([g_disc, d_disc, ai_disc, u_disc], dtype=np.int32)

    def step(self, action):
        alloc = self.allocation[action]
        budget = 1200.0
        Y_before = self._compute_Y()

        self.K = (1 - self.dK) * self.K + alloc[0] * budget
        self.D = (1 - self.dD) * self.D + alloc[1] * budget / 120
        self.AI = (1 - self.dAI) * self.AI + alloc[2] * budget / 25
        self.H = self.H + self.theta_H_acc * alloc[3] * budget / 250 - self.mu * self.H

        shock = np.random.normal(loc=0.0, scale=0.015)
        self.A *= (1 + self.phi1 * self.D + self.phi2 * self.AI + self.phi3 * self.H + shock)
        self.A = max(self.A, 0.10)

        Y_after = self._compute_Y()
        self.growth_rate = (Y_after - Y_before) / (Y_before + 1e-6) * 100

        delta_gdp = self.growth_rate
        unemployment = max(0, 6 - self.growth_rate)
        cyber_risk = max(0, 0.35 * alloc[2] - 0.15 * alloc[3])
        emission = 0.20 * alloc[0] + 0.10 * alloc[2]

        reward = self.w_gdp * delta_gdp - self.w_unemp * unemployment - self.w_cyber * cyber_risk - self.w_emiss * emission

        self.Y_prev = Y_after
        self.t += 1
        terminated = (self.t >= self.T)
        self.state = self._get_state()

        return self.state.copy(), reward, terminated, False, {}

    def render(self):
        print(f"t={self.t} | growth={self.growth_rate:.2f}% | D={self.D:.1f} | AI={self.AI:.1f} | H={self.H:.1f}")

