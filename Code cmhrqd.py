import numpy as np

def m5_risk_assessment(ratios, alloc_matrix_6x4=None, total_budget=50000):
    """
    Module M5 – Đánh giá rủi ro đa chiều (Tối ưu hóa đa mục tiêu).
    f1: GDP Gain (+), f2: Inequality (-), f3: CO2 (-), f4: Cyber Risk (-)
    """
    required_keys = {'K', 'D', 'AI', 'H'}
    assert set(ratios.keys()) == required_keys, "M5: ratios phải gồm K,D,AI,H"
    assert abs(sum(ratios.values()) - 1.0) < 1e-9, "M5: tổng ratios phải bằng 1"
    if total_budget <= 0:
        raise ValueError("M5: total_budget phải dương")

    BETA_MAT = np.array([
        [1.15, 0.85, 0.55, 1.30],
        [0.95, 1.25, 1.40, 1.05],
        [1.05, 0.95, 0.85, 1.15],
        [1.20, 0.75, 0.45, 1.35],
        [0.90, 1.30, 1.55, 1.00],
        [1.10, 0.85, 0.65, 1.25],
    ])
    E_R = np.array([0.42, 0.55, 0.48, 0.32, 0.62, 0.38])
    RHO_R = np.array([0.18, 0.45, 0.28, 0.12, 0.52, 0.22])
    SIG_R = np.array([0.32, 0.28, 0.30, 0.35, 0.25, 0.30])

    if alloc_matrix_6x4 is not None:
        X = np.asarray(alloc_matrix_6x4, dtype=float)
        if X.shape != (6, 4):
            raise ValueError("M5: alloc_matrix_6x4 phải có shape (6,4)")
        if np.any(X < 0):
            raise ValueError("M5: ngân sách không được âm")
    else:
        region_budget = total_budget / 6
        X = np.array([
            [ratios['K'] * region_budget, ratios['D'] * region_budget,
             ratios['AI'] * region_budget, ratios['H'] * region_budget]
            for _ in range(6)
        ])

    budget_actual = float(X.sum())
    if budget_actual <= 0:
        raise ValueError("M5: tổng ngân sách phải dương")

    f1 = float((BETA_MAT * X).sum())

    region_totals = X.sum(axis=1)
    f2 = float(np.abs(region_totals - region_totals.mean()).mean())

    f3 = float((E_R * (X[:, 0] + X[:, 2])).sum())

    f4 = float((RHO_R * X[:, 2]).sum() - (SIG_R * X[:, 3]).sum())

    gdp_per_budget = f1 / budget_actual
    emission_per_budget = f3 / budget_actual
    cyber_per_budget = f4 / budget_actual

    return {
        'f1_gdp_gain': round(f1, 2),
        'f2_inequality': round(f2, 2),
        'f3_emission': round(f3, 2),
        'f4_cyber': round(f4, 2),
        'gdp_per_budget': round(gdp_per_budget, 4),
        'emission_per_budget': round(emission_per_budget, 6),
        'cyber_per_budget': round(cyber_per_budget, 6),
        'total_budget': round(budget_actual, 2)
    }

