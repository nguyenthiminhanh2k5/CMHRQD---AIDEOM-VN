def m4_labor_simulation(ratios, total_budget=BUDGET_LABOR_B12):
    """
    Module M4 – Mô phỏng tác động AI và tự động hóa tới thị trường lao động.
    """

    required_keys = {'K', 'D', 'AI', 'H'}
    assert set(ratios.keys()) == required_keys, "M4: ratios phải gồm K,D,AI,H"
    assert abs(sum(ratios.values()) - 1.0) < 1e-9, "M4: tổng tỷ trọng phải bằng 1"
    if total_budget <= 0:
        raise ValueError("M4: total_budget phải dương")

    sector_names = [
        'Nông-Lâm-Thủy sản', 'CN chế biến', 'Xây dựng', 'Bán buôn-bán lẻ',
        'Tài chính-NH', 'Logistics', 'CNTT-TT', 'Giáo dục-ĐT'
    ]
    labor_force = np.array([13.20, 11.50, 4.80, 7.80, 0.55, 1.95, 0.62, 2.15])
    automation_risk = np.array([18, 42, 25, 38, 52, 35, 28, 22]) / 100

    A1 = np.array([8.5, 32.5, 12.8, 22.4, 45.8, 28.5, 62.5, 18.5])
    B1 = np.array([45.0, 28.0, 35.0, 32.0, 22.0, 30.0, 20.0, 55.0])
    C1 = np.array([5.2, 62.4, 18.5, 48.2, 72.5, 42.8, 32.5, 12.5])

    ai_h_total = ratios['AI'] + ratios['H']
    if ai_h_total < 1e-9:
        ai_share, h_share = 0.5, 0.5
    else:
        ai_share = ratios['AI'] / ai_h_total
        h_share = ratios['H'] / ai_h_total

    labor_share = labor_force / labor_force.sum()
    x_ai = labor_share * ai_share * total_budget
    x_h = labor_share * h_share * total_budget

    new_job = A1 * x_ai
    upgrade_job = B1 * x_h
    displaced_job = C1 * automation_risk * x_ai
    net_job = new_job + upgrade_job - displaced_job

    labor_df = pd.DataFrame({
        'Sector': sector_names,
        'Labor_Million': labor_force,
        'AI_Budget_Billion_VND': np.round(x_ai, 1),
        'H_Budget_Billion_VND': np.round(x_h, 1),
        'NewJob': np.round(new_job, 0),
        'UpgradeJob': np.round(upgrade_job, 0),
        'DisplacedJob': np.round(displaced_job, 0),
        'NetJob': np.round(net_job, 0),
        'NetPositive': net_job >= 0
    })

    total_net_job = float(net_job.sum())
    summary = {
        'total_new': float(new_job.sum()),
        'total_upgrade': float(upgrade_job.sum()),
        'total_displaced': float(displaced_job.sum()),
        'total_net_job': total_net_job,
        'sectors_positive': int((net_job >= 0).sum()),
        'sectors_negative': int((net_job < 0).sum()),
        'net_job_rate_pct': float(100 * total_net_job / labor_force.sum()),
        'best_sector': sector_names[np.argmax(net_job)],
        'worst_sector': sector_names[np.argmin(net_job)]
    }

    return labor_df, summary

